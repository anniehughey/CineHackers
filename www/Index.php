<h2>Latest News</h2>


<h3>October is Silent Movie Month in Ithaca!</h3>
<p>Cornell Cinema joins the Wharton Studio Museum and others to celebrate silent movies this October. We are featuring two spooky classics out of Weimar Germany. F.W. Murnau’s <a href="Fall2016/nosferatu.html">Nosferatu</a>, a tale of a vampire, shown in Sage Chapel with accompaniment by Austin’s The Invincible Czars (Thur Oct 13 @ 7:30); and an recently restored Fritz Lang epic, <a href="Fall2016/destiny.html">Destiny</a>, with piano accompaniment by Dr. Philip Carli (Thur Oct 27 @ 7:00). These are special price events; get your tickets now at <a href="http://CornellCinemaTickets.com">CornellCinemaTickets.com</a>. </p>
<p>And right away in November, we bring back the <a href="series_Fall2016/Alloy.html">Alloy Orchestra</a>, for 4 total silent films with live original scores (Nov 3–5). Get those tickets now and beat the rush!</p>

