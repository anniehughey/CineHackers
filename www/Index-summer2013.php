<h2>Latest News</h2>
 
<h3>Summer 2013</h3>
<p>Willard Straight Theatre will be closed over the summer session  while the space undergoes a major face lift and equipment upgrade. That&rsquo;s right, when you next visit the space, you&rsquo;ll see a beautiful new red velvet stage curtain, a new mechanized screen and&hellip;drum roll&hellip;new DCP (digital cinema package) projection (in addition to our classic reel-to-reel 35mm projection, of course!). These upgrades won&rsquo;t stop us from doing our popular outdoor screenings on the Willard Straight Terrace, though!</p>
<p>Tickets for each show are $12 general / $10 seniors & students, and will be available at <a href="http://CornellCinemaTickets.com">CornellCinemaTickets.com</a> starting Friday, June 14. Tickets purchased online will be held at the will-call table for pick-up the night of the show.</p><p>Unfortunately, due to construction in Willard Straight, we will not be able to offer in-person ticket sales for the  terrace shows - tickets will be available <strong>online only</strong>.
</p>
