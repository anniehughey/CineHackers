<h2>Latest News</h2>
 
<h3>August 2013</h3>
<p>Cornell Cinema reopens Sunday, August 25 in Willard Straight Theatre, a classic movie theatre with new state-of-the-art trappings! </p>
<p>That's right, we have a new, larger mechanized screen; a beautiful new red velvet stage curtain; and…drum roll…new DCP (digital cinema package) projection (in addition to our classic reel-to-reel 35mm projection, of course!).</p>
<p>  In addition to free screenings for new students as part of <a href="series_Fall2013/orientation_week.html">Orientation Week</a>, everyone is invited for just $3 a pop on Sept 1 to experience stunning digital restorations of <a href="Fall2013/dr_strangelove_or_how_i_.html">Dr. Strangelove</a> and <a href="Fall2013/lawrence_of_arabia.html">Lawrence of Arabia</a>!<br>
  </p>
<p>Also, look for the latest edition of our popular Flicksheet publication at a variety of locations on campus and around town.</p>

<h3>Just Added Event</h3>
<p>(note: this event is not in the Flicksheet) </p>
<p><a href="Fall2013/deceptive_practice.html">Deceptive Practice: The Mysteries and Mentors of Ricky Jay</a>, with Ricky Jay in person! Advance tickets available now at <a href="http://CornellCinemaTickets.com">CornellCinemaTickets.com</a>.</p>

<h3>Poster Sale</h3><p> Thur Sept 5 from 11am &ndash; 3pm in the Willard Straight Art Gallery! Decorate your room with posters from your favorite films: Recent-Classic-Cult!!</p>
